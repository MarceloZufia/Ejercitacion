a = 10
b = 3

# Operadores aritméticos
print("Operadores Aritméticos:")
suma    = a + b
print("La suma de ", a, "y", b, "es:", suma)
resta   = a - b 
print("La resta de", a, "y", b, "es:", resta)
mutiplicacion = a * b
print("La multiplicacion de", a, "y", b, "es:", mutiplicacion)
division = a / b
print("La division de", a, "y", b, "es:", division)
modulo = a % b
print("El modulo de", a, "y", b, "es:", modulo)  

# Operadores de comparación
print("\nOperadores de Comparación:") 
igual = (a == b)    
print("¿", a, "es igual a", b, "?:", igual)
mayor_que = (a > b)
print("¿", a, "es mayor que", b, "?:", mayor_que)
menor_que = (a < b)
print("¿", a, "es menor que", b, "?:", menor_que)
mayor_o_igual = (a >= b)
print("¿", a, "es mayor o igual que", b, "?:", mayor_o_igual)
menor_o_igual = (a <= b)
print("¿", a, "es menor o igual que", b, "?:", menor_o_igual)
diferente = (a != b)
print("¿", a, "es diferente de", b, "?:", diferente)    

